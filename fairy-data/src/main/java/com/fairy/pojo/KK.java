package com.fairy.pojo;

/**
 * 卡口
 */
public class KK {
    private String fxdh;
    private String id;
    private String kkId;
    private String kkName;
    private String kkType;
    private String lat;
    private String lng;

    public KK() {
    }

    public String getFxdh() {
        return fxdh;
    }

    public void setFxdh(String fxdh) {
        this.fxdh = fxdh;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKkId() {
        return kkId;
    }

    public void setKkId(String kkId) {
        this.kkId = kkId;
    }

    public String getKkName() {
        return kkName;
    }

    public void setKkName(String kkName) {
        this.kkName = kkName;
    }

    public String getKkType() {
        return kkType;
    }

    public void setKkType(String kkType) {
        this.kkType = kkType;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
}
